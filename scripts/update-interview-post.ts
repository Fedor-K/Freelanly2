async function main() {
  // Configure marked
  marked.setOptions({
    gfm: true,
    breaks: false,
  });

  // Convert markdown to HTML
  const htmlContent = await marked.parse(markdownContent);

  // Add IDs to headings for ToC navigation
  const contentWithIds = htmlContent.replace(
    /<h([2-3])>([^<]+)<\/h[2-3]>/gi,
    (match: string, level: string, text: string) => {
      const id = slugify(text.trim());
      return `<h${level} id="${id}">${text}</h${level}>`;
    }
  );

  // Extract ToC from HTML
  const tocItems: { level: number; text: string; id: string }[] = [];
  const headingRegex = /<h([2-3])[^>]*>([^<]+)<\/h[2-3]>/gi;
  let match;
  while ((match = headingRegex.exec(contentWithIds)) !== null) {
    tocItems.push({
      level: parseInt(match[1]),
      text: match[2].trim(),
      id: slugify(match[2].trim()),
    });
  }

  // Update the post
  const slug = 'how-to-ace-a-remote-job-interview-in-2026';

  const updated = await prisma.blogPost.update({
    where: { slug },
    data: {
      content: contentWithIds,
      tableOfContents: tocItems,
    },
  });

  console.log('Updated blog post:');
  console.log('  Slug:', updated.slug);
  console.log('  ToC items:', tocItems.length);
  console.log('  Content length:', contentWithIds.length, 'chars');
  console.log('  First 500 chars:', contentWithIds.substring(0, 500));
}

